// Operador de negação (!)
// O operador ! inverte o valor booleano

// true se torna false
const macaco = !true;
console.log(macaco); // false

// false se torna true
const banana = !false;
console.log(banana); // true

// O operador ! sempre retorna um valor booleano
const ligado = true;
const desligado = !ligado;

console.log(desligado); // false